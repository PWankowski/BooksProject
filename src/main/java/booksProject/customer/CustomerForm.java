package booksProject.customer;

public class CustomerForm {
    private String name;
    private String surname;
    private int age;
    private String sex;
    private String emailAddress;

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }

    public String getSex() {
        return sex;
    }

    public String getEmailAddress() {
        return emailAddress;
    }
}
